export function normalizeLanguage(value: unknown): AppLanguage {
  return value === 'en-US' || value === 'en' ? 'en-US' : 'zh-CN'
}

export function translate(
  language: AppLanguage,
  source: string,
  params: TranslationParams = {},
): string {
  let template = source
  if (language === 'en-US') {
    template = EN_TRANSLATIONS[source] ?? source
    if (template === source) {
      for (const entry of EN_PATTERN_TRANSLATIONS) {
        const match = entry.pattern.exec(source)
        if (match) {
          template = entry.replace(...match.slice(1))
          break
        }
      }
    }
  }
  return template.replace(/\{(\w+)\}/g, (match, key: string) => (
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match
  ))
}

export function getDateLocale(language: AppLanguage): string {
  return language === 'en-US' ? 'en-US' : 'zh-CN'
}

export function getEnglishTranslations(): Readonly<Record<string, string>> {
  return EN_TRANSLATIONS
}
